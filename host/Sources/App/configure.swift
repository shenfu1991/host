public func runCMD(shell: String) {
    shellFunc(shell)
}

@discardableResult
func shellFunc(_ args: String...) -> Int32 {
    let task = Process()
    task.launchPath = "/usr/bin/env"
    task.arguments = args
    task.launch()
    task.waitUntilExit()
    return task.terminationStatus
}
